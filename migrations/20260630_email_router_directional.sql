-- Катерина: уточнение промпта-роутера — НАПРАВЛЕНИЕ СДЕЛКИ важнее должности отправителя.
-- Боевой кейс: снабженец компании-КЛИЕНТА пишет «необходима к закупу электропечь… есть
-- возможность поставки?» (+ карточка предприятия) — это покупатель НАШЕГО товара (new_request),
-- а модель ловилась на подпись «отдел снабжения» и слова «закуп/поставка» → procurement.
-- Дописываем правило к существующему промпту (контракт маршрутов не меняется → совместимо
-- с уже задеплоенным кодом). Идемпотентно: повторный прогон не дублирует.
UPDATE public.ai_prompts
SET system_prompt = system_prompt || $dir$

РАЗЛИЧАЙ НАПРАВЛЕНИЕ СДЕЛКИ, А НЕ ДОЛЖНОСТЬ ОТПРАВИТЕЛЯ (важно для выбора между "new_request" и "procurement"):
- Отправитель ХОЧЕТ КУПИТЬ наш товар или просит ИЗГОТОВИТЬ/ПОСТАВИТЬ ему продукцию ("нужно", "необходимо к закупу", "есть возможность поставки?", "можете поставить/изготовить", прислал ТЗ/спецификацию/карточку предприятия) — это КЛИЕНТ → "new_request". ДАЖЕ если отправитель из "отдела снабжения"/снабженец и пишет слова "закуп", "снабжение", "поставка". Должность отправителя НЕ делает письмо снабжением.
- "procurement" — ТОЛЬКО когда отправитель САМ ПРЕДЛАГАЕТ ПРОДАТЬ что-то НАМ ("предлагаем", "наш прайс", "готовы поставить вам", каталог поставщика). Признак снабжения: он продаёт НАМ, а не мы ему.$dir$,
    updated_at = now()
WHERE key = 'email_secretary_classifier'
  AND position('НАПРАВЛЕНИЕ СДЕЛКИ' in system_prompt) = 0;
