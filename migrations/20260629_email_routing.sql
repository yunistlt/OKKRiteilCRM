-- Катерина, этап 3: маршрутизация писем по отделам (бухгалтерия / логистика / юрист).
-- В дополнение к «новой заявке → заказ менеджеру» Катерина теперь определяет, к какому
-- отделу относится письмо по СОДЕРЖАНИЮ, и пересылает оригинал (с вложениями) на адрес отдела.
-- Один маршрут на письмо. Адреса отделов — в БД (не хардкод). Дефолт — СУХОЙ ПРОГОН пересылки.

-- 1) Справочник адресов отделов для пересылки (zero-hardcode, имена/адреса — в БД).
--    department — технический код (для логики), label — человеческое имя (для UI).
--    email пуст по умолчанию: пока не заполнен — пересылка в этот отдел не выполняется
--    (помечается needs_review), чтобы письмо не потерялось. Заполнить адреса вручную.
CREATE TABLE IF NOT EXISTS public.email_intake_routes (
    department TEXT PRIMARY KEY,            -- accounting | logistics | legal
    label TEXT NOT NULL,                    -- русское имя отдела (для интерфейса)
    email TEXT,                             -- адрес пересылки (NULL = не настроен)
    is_active BOOLEAN NOT NULL DEFAULT true,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
INSERT INTO public.email_intake_routes (department, label, email) VALUES
    ('accounting',  'Бухгалтерия', NULL),
    ('logistics',   'Логистика',   NULL),
    ('legal',       'Юрист',       NULL),
    ('procurement', 'Снабжение',   NULL)
ON CONFLICT (department) DO NOTHING;

-- 2) Отдельный «аварийный тормоз» для пересылки (независимо от создания заказов).
--    false → СУХОЙ ПРОГОН: маршрут определяется и размечается, но письмо не пересылается.
ALTER TABLE public.email_intake_config
    ADD COLUMN IF NOT EXISTS forward_enabled BOOLEAN NOT NULL DEFAULT false;

-- 3) Поля результата пересылки в журнале писем.
ALTER TABLE public.incoming_emails
    ADD COLUMN IF NOT EXISTS forwarded_department TEXT,   -- accounting | logistics | legal
    ADD COLUMN IF NOT EXISTS forwarded_to TEXT,           -- фактический адрес отправки
    ADD COLUMN IF NOT EXISTS forwarded_at TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS forward_error TEXT;

-- 4) Обновляем промпт классификатора: теперь это МАРШРУТИЗАТОР (один маршрут на письмо).
UPDATE public.ai_prompts SET
    description = 'Катерина-Секретарь: определяет маршрут входящего письма (заявка / бухгалтерия / логистика / юрист / снабжение / не заявка).',
    system_prompt = $prompt$Ты — Катерина, секретарь компании, торгующей металлоконструкциями/шкафами/стеллажами (B2B).
Твоя задача — определить ЕДИНСТВЕННЫЙ маршрут входящего письма по его СОДЕРЖАНИЮ.

Верни ровно один код маршрута (route):

1) "new_request" — НОВАЯ ЗАЯВКА от клиента: запрос коммерческого предложения (КП), счёта, цены,
   наличия, расчёта, сроков изготовления/поставки; ТЗ/спецификация на просчёт; приглашение к участию
   в тендере/закупке. Любое реальное намерение купить НАШУ продукцию / получить НАШЕ предложение.

2) "accounting" — БУХГАЛТЕРИЯ: счета на оплату и подтверждения оплаты, акты сверки, закрывающие
   документы (акты, накладные, УПД, счёт-фактуры), запрос/обмен реквизитов, вопросы по НДС и налогам,
   ЭДО по документам, возврат денежных средств, дебиторка/кредиторка.

3) "logistics" — ЛОГИСТИКА: доставка и отгрузка, сроки и статус доставки, самовывоз, адрес доставки,
   транспортная компания, габариты/вес/упаковка для перевозки, повреждение/недостача при доставке.

4) "legal" — ЮРИСТ: договоры и их согласование, претензии и рекламации с юридическими требованиями,
   суд/иски, штрафы/неустойки/пени, проверка контрагента, юридические запросы и официальные требования.

5) "procurement" — СНАБЖЕНИЕ: письма от ПОСТАВЩИКОВ, которые предлагают/продают товар или услуги НАМ —
   прайсы и коммерческие предложения в наш адрес, предложения о поставке/сотрудничестве, ответы на наши
   запросы закупки, наличие/сроки/условия поставки сырья и материалов.

6) "not_request" — НЕ относится к работе: рекламные рассылки и маркетинг; авто-уведомления (пропущенный
   звонок, голосовая почта, уведомления площадок/порталов); отказ/«неактуально»; нерелевантное.

Правила выбора:
- Маршрут ровно один — выбери НАИБОЛЕЕ подходящий по сути письма.
- Если письмо одновременно про оплату и про новую покупку — приоритет у "new_request" (это новый клиент/сделка).
- Поставщик предлагает товар/услуги НАМ → "procurement" (а не "new_request": это не клиентская заявка).
- Если сомневаешься между отделом и "not_request", и в письме есть реальный рабочий запрос — выбери отдел.

Верни СТРОГО JSON:
{
  "route": "new_request" | "accounting" | "logistics" | "legal" | "procurement" | "not_request",
  "confidence": число от 0 до 1,
  "reasoning": "краткое обоснование на русском (1 предложение)"
}$prompt$,
    metadata = '{"owner":"email_secretary","stage":"production","version":"router_v2"}'::jsonb,
    updated_at = now()
WHERE key = 'email_secretary_classifier';

