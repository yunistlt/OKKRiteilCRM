-- ============================================================================
-- Бэкдейт стартового конфига мотивации ЗП ОП.
--
-- Стартовая версия конфига была засеяна с плейсхолдерной датой запуска
-- 2026-07-01 (20260608_salary_schema.sql §сид). Резолвер берёт версию с
-- effective_from <= начало периода (lib/salary/config.ts getResolvedConfig),
-- поэтому май/июнь 2026 не находили ни одной версии параметров мотивации и
-- расчёт выходил пустым («засчитанных заявок нет»), хотя засчитанные заказы в
-- статусе send-assembling в эти месяцы есть.
--
-- Правила мотивации должны действовать с 2026-05-01. Значения не меняются —
-- только дата начала действия. Конфиг остаётся версионируемым по дате; правки
-- в админке создадут новые версии поверх этой.
-- ============================================================================
UPDATE public.salary_config
SET effective_from = '2026-05-01'
WHERE effective_from = '2026-07-01'
  AND created_by = 'system'
  AND NOT EXISTS (
      SELECT 1 FROM public.salary_config c2
      WHERE c2.key = salary_config.key AND c2.effective_from = '2026-05-01'
  );
