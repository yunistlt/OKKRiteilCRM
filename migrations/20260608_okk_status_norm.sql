-- ============================================================================
-- Фаза 4: норма времени нахождения в статусе — в справочник statuses,
-- редактируется в админке (RetailCRM API reference/statuses её не отдаёт).
-- Убирает хардкод `< 5 дней` из lib/okk-evaluator.ts (checkSLA).
-- ============================================================================

ALTER TABLE public.statuses ADD COLUMN IF NOT EXISTS norm_days INTEGER;

-- Стартовая норма как ДАННЫЕ (не дефолт в коде): сохраняем прежнее поведение
-- (5 дней), дальше руководитель задаёт свою норму на каждый статус в админке.
UPDATE public.statuses SET norm_days = 5 WHERE norm_days IS NULL;
